namespace UF.Controllers
{
    public class Step2Controller : Controller
    {
        private UFDbContext db = new UFDbContext();

        // GET: /Step2/
        public ActionResult Index()
        {
            return View(db.Tests.ToList());
        }

        // GET: /Step2/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Test test = db.Tests.Find(id);
            if (test == null)
            {
                return HttpNotFound();
            }
            return View(test);
        }

        // GET: /Step2/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: /Step2/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include="ID,MONEY,score,Question1,Question2,Question3,Question4,Question5,Question6,UFID")] Test test)
        {
            if (ModelState.IsValid)
            {
                db.Tests.Add(test);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(test);
        }

